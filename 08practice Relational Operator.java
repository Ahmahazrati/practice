package relational.operator;

public class RelarionalOperator {

    public static void main(String[] args) {
        int num1, num2;
        num1 = 10;
        num2 = 14;
        System.out.println(num1 < num2);
        System.out.println(num1 > num2);
        System.out.println(num1 <= num2);
        System.out.println(num2 >= num2);
        System.out.println(num1 == num2);
        System.out.println(num1 != num2);

    }

}
